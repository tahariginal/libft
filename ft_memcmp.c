#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int	r;

	r = 0;
	if (n == 0)
		return (0);
	while (*(t_byte *)s1 == *(t_byte *)s2 && --n)
	{
		s1 ++;
		s2 ++;
	}
	r = *(t_byte *)s1 - *(t_byte *)s2;
	return (r);
}
